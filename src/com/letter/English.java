package com.letter;

public class English {
	public static void main(String[] args) {   
		for (char x = 'a'; x <= 'z'; x++) {
		System.out.print(x +" ");
		if(x == 'z'){
			System.out.println();
			break;
		}
		}
	
	for(char y = 'A'; y<= 'Z'; y++){
		System.out.print(y +" ");
		
	}
		  
	}			    
	
}
